# dict system
# dict=['index','value']


num={'wolds':'lohit','num':23,'school':'spiceup'}

for k in num:
    print(k)

print('-------------------------------------------------------')

for v in num.values():
    print(v)


print('-------------------------------------------------------')

i,j=(1,2)
print(i,j)
print('-------------------------------------------------------')

for i,j in [(1,2),(3,4),(5,6,)]:
    print(i,j)

print('-------------------------------------------------------')

for v in num.items():
    print(v)


d={'wolds':'lohit','num':23,'school':'spiceup'}
